import SwiftUI

extension Color {
    
    static var colorPrimary: Color {
        return Color(UIColor.colorPrimary)
    }
    
    static var colorGreen: Color {
        return Color(UIColor.colorGreen)
    }
    
    static var colorYellow: Color {
        return Color(UIColor.colorYellow)
    }
    
}
