import UIKit

extension UIColor {
    
    static var colorPrimary: UIColor {
        return UIColor(named: "colorPrimary") ?? UIColor.blue
    }
    
    static var colorGreen: UIColor {
        return UIColor(named: "colorGreen") ?? UIColor.green
    }
    
    static var colorYellow: UIColor {
        return UIColor(named: "colorYellow") ?? UIColor.yellow
    }

}
